function buildDialogue(partner, spec, sequence) {
  return DIALOGUE_BUILDERS[sequence - 1](partner, spec);
}

function buildOption(conversationId, optionIndex, partner, spec, option) {
  return {
    id: `${conversationId}-choice-${optionIndex}`,
    label: option.label,
    response: [
      line(partner, `For ${spec.subject}, ${option.action}`),
      line('kiku', `The ${option.label.toLowerCase()} choice respects my stated limit: ${spec.boundary}`),
      line(partner, `The ${option.label.toLowerCase()} decision will be visible when ${spec.observable}`),
    ],
    consequence: {
      flag: `camp.${conversationId}.choice.${optionIndex}`,
      summary: option.summary,
    },
  };
}

function buildConversation(pairId, planned, spec) {
  const [partner] = pairId.split('-');
  return {
    id: planned.id,
    pairId,
    sequence: planned.sequence,
    unlockAfterBeatId: planned.unlockAfterBeatId,
    campId: planned.campId,
    title: spec.title,
    theme: spec.theme,
    dialogue: buildDialogue(partner, spec, planned.sequence),
    choice: {
      prompt: spec.choicePrompt,
      options: spec.choices.map((option, index) => buildOption(planned.id, index + 1, partner, spec, option)),
    },
  };
}

const lateConversations = CAMP_CONVERSATION_GROUPS.late.flatMap((pairId) => {
  const pairPlan = getCampConversationPlan(pairId);
  const specs = SCENE_SPECS[pairId];
  if (!pairPlan || !specs || specs.length !== pairPlan.conversations.length) {
    throw new Error(`Late camp conversation specifications do not match the canonical plan for ${pairId}.`);
  }
  return pairPlan.conversations.map((planned, index) => buildConversation(pairId, planned, specs[index]));
});

export const CAMP_CONVERSATIONS_LATE = deepFreeze(lateConversations);

export const CAMP_CONVERSATIONS_LATE_VALIDATION = validateCampConversationPack(CAMP_CONVERSATIONS_LATE, {
  expectedPairIds: CAMP_CONVERSATION_GROUPS.late,
});

if (!CAMP_CONVERSATIONS_LATE_VALIDATION.ok) {
  throw new Error(`Invalid late camp conversations:\n${CAMP_CONVERSATIONS_LATE_VALIDATION.errors.join('\n')}`);
}
